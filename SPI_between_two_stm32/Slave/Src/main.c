uint8_t r_data[15];
uint8_t true_data[15] = "hello12345aaaaa";

uint8_t r=0;
uint8_t num=2;

uint8_t check()
{
	int right=1;
	for (int i=0;i<15;i++)
		{
			if (r_data[i] != true_data[i])
				right=0;
		}
return right;
}


int main(void)
{

  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_SPI1_Init();
	
	for(int k=0;k<num;k++)
	{
	HAL_SPI_TransmitReceive(&hspi1,s_data,r_data,15,5000);
		if (check()==1)
			r++;
  }
	
	while (1)
  {
				if (r==num)
				HAL_GPIO_TogglePin(LED_GPIO_Port,LED_Pin);
				HAL_Delay(100);
  }

}

