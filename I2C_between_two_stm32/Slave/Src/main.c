#include "main.h"


I2C_HandleTypeDef hi2c2;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_I2C2_Init(void);

uint8_t data[3];

int main(void)
{
  
  HAL_Init();

  SystemClock_Config();

  MX_GPIO_Init();
  MX_I2C2_Init();

  while (1)
  {
				
	while(HAL_I2C_Slave_Receive(&hi2c2,data,3,10) != HAL_OK){}
			
	  data[0] = 170;
		data[1] = 170;		// change data and send it back to Master
		data[2] = 170;
		
		HAL_Delay(2000);

		while(HAL_I2C_Slave_Transmit(&hi2c2,data,3,10) != HAL_OK){}
	
		while(1)	// Blink after complete
		{
				HAL_GPIO_TogglePin(Led_GPIO_Port, Led_Pin);
				HAL_Delay(100);
		}
  }
}


